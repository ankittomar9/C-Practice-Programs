#include<iostream>
using namespace std;

int main(){

        cout<<"The Comparison Operators are explained below:"<<endl;

        int a=4,b=5;

        cout<<"The value of a == b is :"<<(a==b)<<endl;
        cout<<"The value of a != b is :"<<(a!=b)<<endl;
        cout<<"The value of a >= b is :"<<(a>=b)<<endl;
        cout<<"The value of a <= b is :"<<(a<=b)<<endl;
        cout<<"The value of a > b is :"<<(a>b)<<endl;
        cout<<"The value of a < b is :"<<(a<b)<<endl;

        return 0;

}