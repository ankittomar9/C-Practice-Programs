#include<iostream>

using namespace std;

int main(){

    int a = 10;
    int b = 3;

    cout<<"The value of a + b : "<<a+b<<endl;
    cout<<"The value of a - b : "<<a-b<<endl;
    cout<<"The value of a * b : "<<a*b<<endl;
    cout<<"The value of a / b : "<<a/b<<endl;
    cout<<"The value of a % b : "<<a%b<<endl;//10
    cout<<"The value of a++ is : "<<a++<<endl;//11
    cout<<"The value of a-- is : "<<a--<<endl;
    cout<<"The value of ++a is : "<<++a<<endl;
    cout<<"The value of --a is : "<<--a<<endl;
    
    return 0;


}